package array;

public class A6_TwoDimensionArray {
	public static void main(String[] args) {
		               //row col
		int[][]a=new int[2][3];
		a[0][0]=1;
		a[0][1]=2;
		a[0][2]=3;
		a[1][0]=4;
		a[1][1]=5;
		a[1][2]=6;
System.out.println(a.length);//2
System.out.println(a[0].length);//3
System.out.println(a[1][1]);//5
	}

}
